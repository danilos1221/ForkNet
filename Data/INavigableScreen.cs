/// <summary>
/// Экран телефона, у которого есть собственная внутренняя навигация
/// (например ChatPanel: список чатов <-> окно чата).
/// Позволяет PhoneNavigationManager делегировать обработку "назад"
/// самому экрану, прежде чем уходить на предыдущий экран в истории.
/// </summary>
public interface INavigableScreen
{
    /// <summary>
    /// Попытаться обработать "назад" внутри себя.
    /// Возвращает true, если обработал (PhoneNavigationManager остаётся на этом экране).
    /// Возвращает false, если внутри обрабатывать нечего — переход к предыдущему экрану.
    /// </summary>
    bool TryHandleBack();
}